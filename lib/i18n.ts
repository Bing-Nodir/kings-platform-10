export type TranslationKeys = typeof translations.uz;

export function t(locale: Locale, section: keyof TranslationKeys, key: string): string {
  const sectionData = translations[locale]?.[section] as Record<string, unknown> | undefined;
  if (sectionData && typeof sectionData[key] === "string") {
    return sectionData[key] as string;
  }
  // Fallback to UZ
  const fallback = translations.uz[section] as Record<string, unknown> | undefined;
  if (fallback && typeof fallback[key] === "string") {
    return fallback[key] as string;
  }
  return key;
}
